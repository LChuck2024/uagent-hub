/** Bundle Edge Function + shared handlers into edge-functions/api/[[default]].js */
import esbuild from "esbuild";
import path from "path";
import { fileURLToPath } from "url";

const root = path.dirname(path.dirname(fileURLToPath(import.meta.url)));
const outDir = path.join(root, "edge-functions", "api");
const outFile = path.join(outDir, "[[default]].js");

await esbuild.build({
  entryPoints: [path.join(root, "edge-functions/_src/api.mjs")],
  bundle: true,
  platform: "neutral",
  format: "esm",
  outfile: outFile,
  loader: { ".ts": "ts" },
  banner: {
    js: "/** Auto-generated by scripts/bundle-edge-api.mjs — edit edge-functions/_src/ instead */",
  },
});

console.log("Bundled edge-functions/api/[[default]].js (edge-v1)");
